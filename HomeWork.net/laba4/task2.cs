using System;

class A
{
    protected int a;
    protected int b;

    // Конструктор класу А
    public A()
    {
        a = 5; // Приклад значення для поля a
        b = 3; // Приклад значення для поля b
    }
}

class B : A
{
    private int d;

    // Конструктор класу B, успадкований від конструктора класу А
    public B() : base()
    {
        d = 7; // Приклад значення для поля d
    }

    // Власний конструктор класу B
    public B(int aVal, int bVal, int dVal)
    {
        a = aVal; // Ініціалізація поля a з параметра конструктора
        b = bVal; // Ініціалізація поля b з параметра конструктора
        d = dVal; // Ініціалізація поля d з параметра конструктора
    }

    // Властивість с2 - результат обчислення виразу над полями a, b, d
    public int C2
    {
        get
        {
            int result;
            // Приклад використання керуючого оператора
            if (a > b)
                result = a * b + d;
            else
                result = a + b - d;
            return result;
        }
    }
}

class Program
{
    static void Main(string[] args)
    {
        // Створюємо об'єкт класу А
        A objA = new A();

        // Виводимо значення властивості c2 класу A на екран
        // Console.WriteLine("Значення властивості c2 для класу A: " + objA.C2); // Помилка! Клас A не має властивості C2.

        // Створюємо об'єкт класу B за допомогою конструктора, успадкованого від класу A
        B objB1 = new B();

        // Виводимо значення властивості c2 класу B на екран
        Console.WriteLine("Значення властивості c2 для класу B (конструктор успадкований від класу A): " + objB1.C2);

        // Створюємо об'єкт класу B за допомогою власного конструктора
        B objB2 = new B(2, 4, 6);

        // Виводимо значення властивості c2 класу B на екран
        Console.WriteLine("Значення властивості c2 для класу B (власний конструктор): " + objB2.C2);
    }
}
