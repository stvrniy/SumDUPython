# Зчитування значень a та b з клавіатури
a = float(input("Введіть значення a: "))
b = float(input("Введіть значення b: "))

# Перевірка на додатність (згідно з варіантом)
if a <= 0 or b <= 0:
    print("Помилка: a та b повинні бути додатніми числами.")
else:
    # Обчислення значення X згідно з варіантом 13
    if a > b:
        X = b / a + 61
    elif a == b:
        X = -5
    else:
        X = (b - a) / b

    # Виведення результату
    print("Значення X:", X)
